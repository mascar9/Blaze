package com.example.shapeforge;

public class SetRep {

    private int Set, Rep;

    public SetRep(int Set, int Rep){
        this.Rep = Rep;
        this.Set = Set;
    }

    public SetRep(){

    }


    public int getRep() {
        return Rep;
    }

    public int getSet() {
        return Set;
    }
}
